void HAL_ADC_ConvHalfCpltCallback(ADC_HandleTypeDef *hadc)
{
	for(uint16_t AvCount = 0; AvCount <= HALF_BUFF_LEN; AvCount++)
	{
		AudioSampleAccum1 = AudioSampleAccum1 + MyMicValues[AvCount];
	}
	AudioAv1 = AudioSampleAccum1 >> 7;
	AudioSampleAccum1 = AudioAv1; //running audio average
}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc)
{
	for(uint16_t AvCount = HALF_BUFF_LEN; AvCount <= BUF_LEN; AvCount++)
	{
		AudioSampleAccum2 = AudioSampleAccum2 + MyMicValues[AvCount];
	}
	AudioAv2 = AudioSampleAccum2 >> 7;
	AudioAvTotal = (AudioAv1 + AudioAv2) >> 1;
	AudioSampleAccum2 = AudioAv2; //running audio average
	NewAudioSampleFLAG = 1;

}

